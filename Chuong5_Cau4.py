'''
Một số hàm quan trọng trong xử lý chuỗi của Python:
1. len(): Trả về độ dài của chuỗi.
2. lower(): Chuyển đổi tất cả các ký tự trong chuỗi thành chữ thường.
3. supper(): Chuyển đổi tất cả các ký tự trong chuỗi thành chữ hoa.
4. title(): Chuyển đổi ký tự đầu của mỗi từ thành chữ hoa.
5. capitalize(): Chuyển đổi ký tự đầu tiên của chuỗi thành chữ hoa.
6. swapcase(): Đổi chữ hoa thành chữ thường và ngược lại.
7. strip(): Loại bỏ khoảng trắng ở đầu và cuối chuỗi.
8. lstrip(): Loại bỏ khoảng trắng bên trái.
9. rstrip(): Loại bỏ khoảng trắng bên phải.
10. center(), ljust(), rjust(): Căn giữa, căn trái, căn phải chuỗi. 
11. find() và index(): Tìm vị trí của một chuỗi con trong chuỗi khác và trả về vị trí đầu tiên.
12. split(): Tách chuỗi thành nhiều chuỗi con
13. join(): Nối các chuỗi con thành một chuỗi lớn.
14. replace(): Thay thế một chuỗi con bằng một chuỗi con khác.

'''